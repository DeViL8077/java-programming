public class arithmetic_operations {
    public static void main(String[] args) {
        int a=9;
        int b=13;
        int c;
        c=a+b;
        System.out.println(c);
        c=a-b;
        System.out.println(c);
        c=a*b;
        System.out.println(c);
        c=a/b;
        System.out.println(c);
        c=a%b;
        System.out.println(c);
        
    }
            
}
